export function getSentencesByCategory(category) {
  return SENTENCES.filter(s => s.category === category);
}

export function getSentencesByLevel(level) {
  return SENTENCES.filter(s => s.level === level);
}

export function getRandomSentence(category = null) {
  const pool = category ? SENTENCES.filter(s => s.category === category) : SENTENCES;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getExamText(durationMinutes = 1) {
  // 검정용 긴 문장 조합
  const examSentences = SENTENCES.filter(s => s.category === '검정');
  const all = SENTENCES.filter(s => s.level >= 2);
  const shuffled = [...all].sort(() => Math.random() - 0.5);
  const targetLength = durationMinutes * 300; // 분당 약 300자 기준
  let text = '';
  let i = 0;
  while (text.length < targetLength) {
    text += shuffled[i % shuffled.length].text + ' ';
    i++;
  }
  return text.trim();
}
